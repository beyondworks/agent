export function getSceneSplittingPrompt(fullScript: string, contentType: 'short_form' | 'long_form') {
  const maxScenes = contentType === 'short_form' ? 20 : 60;
  const durationRule = contentType === 'short_form'
    ? '**각 씬은 반드시 3~5초. 5초를 절대 초과하지 마라.** 나레이션이 길면 여러 씬으로 쪼개라. 한 씬에 2문장 이상 넣지 마라.'
    : '각 씬은 5~15초. 시간차 애니메이션으로 정보를 제공하며 멈춰있는 장면 없이 항상 동적이어야 한다.';

  return `다음 대본을 ${maxScenes}개 이내의 씬으로 분할하세요.

**씬 길이 규칙**: ${durationRule}

대본:
${fullScript}

각 씬에 대해 다음을 JSON 형식으로 제공하세요:
- scene_number: 씬 번호 (1부터)
- narration: 이 씬에서 읽을 나레이션 텍스트 (숏폼은 1문장 이하)
- duration_sec: 나레이션 길이 기준 (한국어 초당 약 2.5어절). ${contentType === 'short_form' ? '반드시 3~5초 범위' : '5~15초 범위'}
- keywords: 핵심 키워드 2-3개

CRITICAL: ${contentType === 'short_form' ? '숏폼은 각 씬 3~5초 필수. 5초 초과 씬이 하나라도 있으면 실패.' : '롱폼은 5~15초 범위 유지.'}
시각 설명과 이미지 프롬프트는 이 단계에서 생성하지 마세요. 나레이션 분할에만 집중하세요.`;
}
