export function getOutlinePrompt(params: {
  analysis: string;
  keywords: string[];
  contentType: 'short_form' | 'long_form';
  targetDuration: number;
}) {
  const structure =
    params.contentType === 'short_form'
      ? ['hook(3초)', 'problem(10초)', 'solution(30초)', 'cta(10초)']
      : ['hook(15초)', 'intro(60초)', 'body_sections(N개)', 'conclusion(60초)', 'cta(30초)'];

  return `키워드: ${params.keywords.join(', ')}
콘텐츠 유형: ${params.contentType === 'short_form' ? '숏폼' : '롱폼'}
목표 길이: ${params.targetDuration}초
구조: ${structure.join(' → ')}

분석 자료:
${params.analysis}

아웃라인을 작성하세요.`;
}

